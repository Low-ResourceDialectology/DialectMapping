#  GADM36 Code list (Global, Region, Country)
https://data.apps.fao.org/catalog/dataset/gadm36-code-list-global-region-country
GADM, the Database of Global Administrative Areas, is a high-resolution database of country administrative areas, with a goal of providing maps and spatial data for all countries and their sub-divisions.
This dataset contains the codelist for the GADM version 3.6 boundaries at Country Level. To use this codelist on the HiH Geospatial Platform use the column name gadm36_0 in your tabular data.




